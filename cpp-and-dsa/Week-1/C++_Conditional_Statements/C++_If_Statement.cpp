// The C++ if Statement is the most basic decision-making statement.
// It allows you to execute a block of code only if a specified condition is true.

#include <iostream>
using namespace std;

int main() {
    int i = 10;
    // If statement
    if (i < 15) {
        cout << "10 is less than 15" << endl;
    }

    return 0;
}